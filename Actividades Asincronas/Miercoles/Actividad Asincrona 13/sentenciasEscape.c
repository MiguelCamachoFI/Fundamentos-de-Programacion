#include<stdio.h>
int main()
{
    //Salto de línea
    printf("---Salto de línea---\n");
    printf("Hola mundo!!!\n");
    printf("Bienvenidos al curso!!!\n\n");

    //Tabulador horizontal
    printf("---Tabulador horizontal---\n");
    printf("Hola mundo!!!\t");
    printf("Bienvenidos al curso!!!\n\n");

    //Caracter de alarma
    printf("---Caracter de alarma---\n");
    printf("Hola mundo!!!\a");
    printf("Bienvenidos al curso!!!\n\n");

    //Retroceso de carror
    printf("---Retroceso de carror---\n");
    printf("Hola mundo!!!\r");
    printf("Bienvenidos al curso!!!\n\n");

    //Retroceso
    printf("---Retroceso---\n");
    printf("Hola mundo!!!\b");
    printf("Bienvenidos al curso!!!\n\n");

    return 0;
}